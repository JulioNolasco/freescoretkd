package FreeScore::History;
use base qw( FreeScore::Component );

# ============================================================
sub init {
# ============================================================
	my $self   = shift;
	my $parent = shift;
	$self->SUPER::init( $parent );
	$self->{ undo } = [];
	$self->{ redo } = [];
}

# ============================================================
sub clear_redo {
# ============================================================
	my $self = shift;
	my @redo = @{ $self->{ redo }};
	$self->{ redo } = [];
}

# ============================================================
sub clear_undo {
# ============================================================
	my $self = shift;
	my @undo = @{ $self->{ undo }};
	$self->{ undo } = [];
}

# ============================================================
sub push_redo {
# ============================================================
	my $self = shift;
	my @sql  = @_;
	push @{$self->{ redo }}, @sql;
}

# ============================================================
sub push_undo {
# ============================================================
	my $self = shift;
	my @sql  = @_;
	push @{$self->{ undo }}, @sql;
}

1;
